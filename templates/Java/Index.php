<div class="mt-20 text-gray-700 w-full rounded-2xl shadow-2xl bg-zinc-50 text-center p-10">
    <p class="mt-3 mb-10">
        <strong>Java란 무엇인가?</strong><br>
    </p>
    <p class="text-justify">
        Java는 가능한 한 적은 구현 종속성을 가지도록 설계된 고수준의 클래스 기반 <span class="text-red-400 font-bold">객체 지향</span> 프로그래밍 언어입니다.<br>
        이 언어는 '한 번 작성하면, 어디서나 실행할 수 있다'는 (WORA) 원칙을 따르며,<br>
        한 번 컴파일된 Java 코드는 재컴파일 없이 Java를 지원하는 모든 플랫폼에서 실행될 수 있도록 설계된 범용 프로그래밍 언어입니다.
    </p>
    <p class="text-justify mt-10">
        Java 애플리케이션은 일반적으로 바이트코드로 컴파일되며,<br>
        이 바이트코드는 기본 컴퓨터 아키텍처와 상관없이 모든 Java 가상 머신(JVM)에서 실행될 수 있습니다.<br>
        Java의 문법은 C++과 유사하며, 단순하고 사용하기 쉽게 설계되었습니다.
    </p>
    <p class="text-justify mt-10">
        2019년 기준으로 Java는 GitHub에 따르면 특히 클라이언트-서버 웹 애플리케이션에서 <span class="text-red-400 font-bold">가장 인기</span> 있는 프로그래밍 언어 중 하나로,<br>
        약 900만 명의 개발자가 사용하는 것으로 보고되었습니다.
    </p>
    <p class="text-justify mt-10">
        Java초급부터 중급, 고급 그리고 Spring Framework까지 다양한 주제로 Java에 대해 알아보겠습니다.
    </p>
